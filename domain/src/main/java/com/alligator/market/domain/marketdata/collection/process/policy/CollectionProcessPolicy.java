package com.alligator.market.domain.marketdata.collection.process.policy;

import java.time.Duration;

/**
 * Общий контракт политики процесса сбора рыночных данных.
 *
 * <p>Политика задана интерфейсом намеренно: процессы сбора могут существенно отличаться по механике работы,
 * источникам данных и параметрам управления. Поэтому общий контракт фиксирует только базовые ограничения,
 * а конкретные процессы расширяют его собственными policy-моделями.</p>
 */
public interface CollectionProcessPolicy {

    /**
     * Минимально допустимый интервал между фиксациями тиков для данного процесса.
     */
    Duration minCaptureInterval();
}
